const char** 
CAttentionDetection::GetFilterBankString(int sampling_rate)
{
	int fk = 0;
	int df = MAXINT;
	for (int fi = 0; FiltFilt_FilterBankString[fi]; fi++) {
		int iv = atoi(FiltFilt_FilterBankString[fi++]);
		iv -= sampling_rate;
		if (iv < 0) iv = -iv;
		if (iv < df) {
			df = iv;
			fk = fi;
		}

		// jump this bank
		while (FiltFilt_FilterBankString[fi]) fi++;
	}

	return FiltFilt_FilterBankString + fk;
}

